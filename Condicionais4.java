public class Condicionais4 {
    public static void main(String[] args) {
        int x = 3;

        switch(x) {

            case 1:
            System.out.println("x é igual a 1");
            System.out.println("x é igual ou maior que 0");
            break;

            case 2:
            System.out.println("x é igual a 2");
            System.out.println("x é igual ou maior que 0");
            break;

            case 3:
            System.out.println("x é igual a 3");
            System.out.println("x é igual ou maior que 0");
            break;

            default:
                System.out.println("x é maior que 3");
                System.out.println("x é maior ou igual a 0");
        }
    }
}
